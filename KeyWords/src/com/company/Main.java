package com.company;

public class Main {

    public static void main(String[] args) {

        // Expression is everything after the data type.
        // data type / expression
        float myNumber = 77;

        // Expression is in the brackets
        if ( myNumber > 70 ) {
            System.out.println("Hello World");
        }

        // Entire line is a statement
        int number = 9;
        System.out.println("Statement");
        System.out.println("this" +
                "is a statement" +
                "as well");

    }
}
